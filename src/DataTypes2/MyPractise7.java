package DataTypes2;

public class MyPractise7 {
    public static void main(String[] args) {
        int a = 53;
        int tam = a/10; // 5
        System.out.println("tam="+tam);

        int qaliq = a%10; // 3
        System.out.println("qaliq="+qaliq);

        System.out.println("-------------");

        double b = 51;

        double tam2 = b/10; // 5.1
        System.out.println("tam="+tam2);

        int tam3 = (int) b/10; // 5
        System.out.println("tam="+tam3);

        double qaliq2 = b%10; // 1.0
        System.out.println("qaliq="+qaliq2);

        int qaliq3 = (int) b%10; // 1
        System.out.println("qaliq="+qaliq3);



    }
}
