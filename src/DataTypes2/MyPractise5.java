package DataTypes2;

public class MyPractise5 {
    public static void main (String [] args){

        int a = 4 ;

        a++; // 5
        System.out.println(a);

        a--; // 4
        System.out.println(a);

        a+=2; // a=a+2 deməkdir yəni vahid artır
        System.out.println(a);

        a-=5; // a=a-5 deməkdir yəni 5 vahid azalır
        System.out.println(a);






    }
}
