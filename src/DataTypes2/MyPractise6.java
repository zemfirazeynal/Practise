package DataTypes2;

public class MyPractise6 {
    public static void main(String[] args) {
        // / v…ô %

        int a = 75;

        int b = a/10;
        System.out.println(b); // 7

        double c = a/10;
        System.out.println(c); // 7.0

        double c1 = (double) a /10;
        System.out.println(c1); // 7.3


        double d = 73;

        double c2 = d/10;
        System.out.println(c2); // 7.3


        int e = 85;

        int f = e%10;
        System.out.println(f); // 5



    }
}
